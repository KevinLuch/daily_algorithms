// Create a function that returns true if an integer is evenly
// divisible by 5, and false to otherwise.

// Examples:

// divisibleByFive(5) ➞ true
// divisibleByFive(-55) ➞ true
// divisibleByFive(37) ➞ false

// **************************************************************

function divisibleByFive(n) {
	if(n % 5 === 0){
		return true
	}
	return false 
}

