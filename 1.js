function print1to255() {
    var num = 1;
    while (num <= 255) {
        console.log(num)
        num = num + 1;
    }
}

print1to255();

// **************************************************************

function print1to255v2() {

    for (var num = 0; num <= 255; num++) {
        console.log(num)
    }
}

print1to255v2();

