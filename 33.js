// Given two arguments, return an array which contains
// these two arguments

// Examples:

// makePair(1, 2) ➞ [1, 2]
// makePair(51, 21) ➞ [51, 21]
// makePair(512124, 215) ➞ [512124, 215]
// **************************************************************

const makePair = (num1, num2) => {
    const result = [];
    result.push(num1, num2);
    return result;
}

